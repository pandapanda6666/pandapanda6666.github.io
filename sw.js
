// 💡 Firebase 萬用網域代理晶片
self.addEventListener('fetch', function(event) {
  let requestUrl = event.request.url;
  
  // 💡 關鍵：當那 200 個網頁去敲 GitHub API 時，如果發現網址結尾帶有斜線，自動在背景把斜線切掉！
  if (requestUrl.includes('://github.com') && requestUrl.endsWith('/')) {
    requestUrl = requestUrl.replace(/\/+$/, '');
    console.log('--- 🚀 萬用晶片已成功校正 GitHub API 網址為: ' + requestUrl + ' ---');
    event.respondWith(fetch(requestUrl, { headers: event.request.headers }));
    return;
  }
  
  event.respondWith(fetch(event.request));
});
